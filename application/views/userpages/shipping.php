<h2><strong>Памятка покупателю</strong></h2>
<ul>
<li>забрать гарантийные талоны с печатью и инструкции по эксплуатации, если прилагается к данному товару.</li>
<li>при получении товара просим внимательно проверить: комплектацию,  повреждения, качество (после отгрузки претензий по количеству и качеству товара не принимаются)</li>
</ul>
<h2><strong>Вместе с заказом Вам необходимо</strong></h2>
<ul>
	<li><strong>для физических лиц:</strong> взять у водителя товарный и кассовый чек, а так же, необходимо расписаться в маршрутном листе, для подтверждения того, что Вы приняли товар надлежащего вида</li>
	<li><strong>для юридических лиц:</strong> взять у водителя оригинал счета, накладную, счет-фактуру; необходимо расписаться и поставить печать в расходной накладной, или приложить доверенность с печатью</li>
	
